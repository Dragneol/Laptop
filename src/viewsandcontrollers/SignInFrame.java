    private boolean valid3Field(String user, String pass, String repass) {
        boolean isCorrect = true;
        if (user.length() == 0 || pass.length() == 0 || repass.length() == 0) {
            isCorrect = false;
        }
        if (dao.getAccountByUser(user) != null) {
            isCorrect = false;
        }
        if (!pass.equals(repass)) {
            isCorrect = false;
        }
        return isCorrect;
    }

