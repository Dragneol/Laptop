    private void btnSignInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignInActionPerformed
        // TODO add your handling code here:

        String username = txtUsername.getText();
        String password = new String(txtPassword.getPassword());
        String repassword = new String(txtRePass.getPassword());
        dto = new UserDTO(username, password);
        boolean inserted;
        if (valid3Field(username, password, repassword)) {
            inserted = dao.inserNew(dto);
        } else {
            util.Message.showMessage("Sign In Failed! Please check following condition\n"
                    + "1/ All Field must not to be blank\n"
                    + "2/ Re-Password field is not exactly with Password"
                    + "3/ Username have been registed");
            return;
        }
        if (inserted) {
            util.Message.showMessage("Succesfull! Please waiting for admin's acception");
        } else {
            util.Message.showMessage("There an error occur. Please contact with admin for more detail");
        }
    }//GEN-LAST:event_btnSignInActionPerformed

